use anyhow::Result;
use std::thread;
use std::time::Duration;

/*
 * The dynamic loader generated by `build.rs` is injected into an isolated 
 * module namespace. This prevents trait and import pollution inside the main
 * daemon.
 */
#[allow(clippy::all)]
pub mod bpf_loader {
    include!(concat!(env!("OUT_DIR"), "/bpf_loader.rs"));
}

fn main() -> Result<()> {
    println!("🛡️ Starting Bouclier Bleu Core Engine...");

    /*
     * eBPF programs remain attached to kernel hooks ONLY as long as their 
     * corresponding file descriptors remain open in userland.
     * By storing the returned skeletons in `_active_skeletons`, we tie the 
     * lifetime of the kernel security hooks to the lifetime of this Rust
     * process.
     */
    let _active_skeletons = bpf_loader::load_all_modules()?;

    println!("· [Success] All defensive eBPF modules loaded and attached.");
    println!("· Engine is running securely.");
    println!("· Run: # cat /sys/kernel/tracing/trace_pipe in another terminal to see alerts.");
    println!("Press Ctrl+C to exit.");
    /*
     * The daemon must remain active to maintain the BPF file descriptors.
     * In a production deployment, this sleep loop should be replaced by an
     * asynchronous runtime (e.g., Tokio) polling a `libbpf_rs::RingBuffer` for
     * structured kernel telemetry.
     */
    loop {
        thread::sleep(Duration::from_secs(1));
    }
}
